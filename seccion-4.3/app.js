let dia = 5;
switch (dia) {
  case 0:
    console.log("Es domingo");
    break;
  case 1:
    console.log("Es lunes");
    break;
  case 2:
    console.log("Es martes");
    break;
  case 3:
    console.log("Es miercoles");
    break;
  case 4:
    console.log("Es jueves");
    break;
  case 5:
    console.log("Es viernes");
    break;
  case 4:
    console.log("Es sabado");
    break;
  default:
    console.log("El dia Ingresado no es permitido");
}
